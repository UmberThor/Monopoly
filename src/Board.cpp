//Funzione per il riempimento dello std::vector<Cell> con proprieta' di tipo casuale
void Board::initialize_cells(int c_e, int c_s, int c_d)
{
    std::vector<int> property_types;
    
    for (int i = 0; i < 24; i++)
    {
        property_types.push_back(i);
    }
    
    std::random_shuffle(property_types.begin(), property_types.end());

    for (int i = 0; i < 28; i++)
    {
        
        if (i % 7 == 0)
        {
			Cell *a = new Cell(i, convert_coordinates(i), i==14);
			cells.push_back(a);
        }
        else    
        {
            int type = property_types.back();
            property_types.pop_back();

            if (type < c_e)
            {
                cells.push_back(new Economy(i, convert_coordinates(i)));
            }
            else if (type < c_e + c_s)
            {
				cells.push_back(new Standard(i, convert_coordinates(i)));
			}
            else
            {
				cells.push_back(new Deluxe(i, convert_coordinates(i)));
			}
        }
    }
}

//Funzioni di appoggio per la gestione del tabellone a matrice
int get_index(int a, int b) 
{
    if (a == 0 || b==7)
    {
        return b + a;
    }
    else if (a == 7 || b == 0)
    {
        return 28 - a - b;
    }
    else
    {
        return -1;
    }
}

std::array<int, 2> get_coordinates(int i) 
{
    std::array<int, 2> c;

    if (i <= 7) 
    {
        c[0] = 0;
        c[1] = i;
    }
    else if (i <= 14) 
    {
        c[0] = i - 7;
        c[1] = 7;
    }
    else if (i <= 21) 
    {
        c[0] = 7;
        c[1] = 21 - i;
    }
    else if (i <= 27) 
    {
        c[0] = 28 - i;
        c[1] = 0;
    }
    return c;
}

std::string convert_coordinates(int pos)
{
	std::string values = {"ABCDEFGH12345678"};
	std::string coordinates = "";

	switch (pos / 7)
	{
		case 0:
			coordinates = "A" + std::string(1, values[8 + pos % 7]);
			break;
		case 1:
			coordinates = std::string(1, values[pos % 7]) + "8";
			break;
		case 2:
			coordinates += "H" + std::string(1, values[15 - pos % 7]);
			break;
		case 3:
			coordinates += std::string(1, values[7 - pos % 7]) + "1";
			break;
		default:
			break;
	}
	return coordinates;
}

int length_no_color(const std::string &input)
{
    int count = 0;
    bool in_color_code = false;

    for (char ch : input)
    {
        if (ch == '\033')
        {
            in_color_code = true;
        }
        else if (in_color_code && ch == 'm')
        {
            in_color_code = false;
        }
        else if (!in_color_code)
        {
            count++;
        }
    }
    return count;
}

//Visualizzazione console del tabellone di gioco
void Board::show_board() const
{
    std::vector<std::string> cell_content(28);

    for (int i = 0; i < 28; i++) 
    {
        if (cells.at(i)->get_owner() != nullptr)
        {        
            cell_content[i] = cells.at(i)->get_owner()->color;
        }
        
        cell_content[i] += " " + std::string(1, cells.at(i)->get_type());        

        if (cells.at(i)->get_building() == house)
        {
            cell_content[i] += "*";
        }
        else if (cells.at(i)->get_building() == hotel)
        {
            cell_content[i] += "^";
        }
        
        cell_content[i] += "\033[0m";

        for (int p = 0; p < players.size(); p++) 
        {
            if (players.at(p)->get_current_cell_index() == i)
            {
                cell_content[i] += (players.at(p)->color + players.at(p)->name + "\033[0m");
            }
        }
    }

    std::array<int, 8> column_width = {};

    for (int i = 0; i < 28; i++) 
    {
        int l = length_no_color(cell_content[i]);
        
        if (l > column_width[get_coordinates(i)[1]])
        {
            column_width[get_coordinates(i)[1]] = l;
        }
    }
    
    std::cout << "\n";
    
    //Visualizzazione dei riferimenti alfanumerici alle caselle del tabellone
    std::cout<<"\033[2m";
    std::cout << "     ";
    for(int k = 0; k < 8; k++)
    {
    	int left = (column_width[k] + 2)/2;
    	int right = column_width[k] + 1 - left;
    	
    	for(int m = 0; m < left; m++)
    	{
    		std::cout << " ";
    	}
    	
    	std::cout << k + 1;
    	
    	for(int m = 0; m < right; m++)
    	{
    		std::cout << " ";
    	}
    }
    
    std::cout << '\n';
    std::string letters = {"ABCDEFGH"};

    for (int r = 0; r < 8; r++) 
    {
    	std::cout<<"\033[2m";
        std::cout<<letters[r]<<"    ";
        std::cout << "\033[0m";
        
        for (int c = 0; c < 8; c++)
        {            
            if (c==0)
            {
                std::cout << "|";
            }
            
            if (get_index(r,c) == -1) 
            {
                int offset = 2;
                
                if (c == 6)
                {
                    offset = 1;
                }
                
                for (int k = 0; k < column_width[c] + offset; k++)
                {
                    std::cout << " ";
                }
                
                if (c == 6)
                {
                    std::cout << "|";
                }
            }
            else 
            {
                std::cout << cell_content[get_index(r, c)];

                for (int k = 0; k < column_width[c] - length_no_color(cell_content[get_index(r,c)]); k++)
                {
                    std::cout << " ";
                }
                
                std::cout << " |";
            }
        }
        
        std::cout << "\n";
    }
    
    std::cout << "\n";
}

